/* eslint-disable @typescript-eslint/no-explicit-any */
export type ActionMap<M extends { [index: string]: any }> = {
  [Key in keyof M]: M[Key] extends undefined
    ? {
        type: Key
      }
    : {
        type: Key
        payload: M[Key]
      }
}

export type ValueOf<T> = T[keyof T]
